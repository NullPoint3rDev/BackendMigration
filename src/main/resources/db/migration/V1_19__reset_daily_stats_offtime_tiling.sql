-- Таймеры активности теперь замощают сутки полностью: непокрытое телеметрией время (голова/хвост/
-- отсутствие пакетов) идёт в «Выкл.». Сбрасываем кэш, чтобы суммы сошлись с реальным временем.
UPDATE welding_machine_daily_stats
SET computed_at = TIMESTAMP '1970-01-01 00:00:00'
WHERE stat_date >= CURRENT_DATE - INTERVAL '30 days';
